def translate_name(name):
    """将英文知识点名称翻译为中文，支持中英混合文本"""
    
    # 1. 先查短语词典（长短语优先）
    sorted_phrases = sorted(PHRASE_DICT.items(), key=lambda x: -len(x[0]))
    for phrase, cn in sorted_phrases:
        if phrase in name.lower():
            name = re.sub(re.escape(phrase), cn, name, flags=re.IGNORECASE)
    
    # 2. CamelCase 拆分: "ATPSynthase" -> "ATP Synthase"
    name = re.sub(r'([a-z])([A-Z])', r'\1 \2', name)
    name = re.sub(r'([A-Z]+)([A-Z][a-z])', r'\1 \2', name)
    
    # 3. 在中英文边界处插入空格，使得后续能正确分词
    # "氨基酸分类And性质" -> "氨基酸分类 And 性质"
    name = re.sub(r'([\u4e00-\u9fff])([A-Za-z])', r'\1 \2', name)
    name = re.sub(r'([A-Za-z])([\u4e00-\u9fff])', r'\1 \2', name)
    
    # 4. 分词翻译
    words = re.split(r'\s+', name)
    translated_parts = []
    
    SKIP_WORDS = {"and", "or", "the", "of", "in", "on", "at", "to", "for", "with",
                  "by", "from", "an", "a", "its", "their", "via", "vs", "between"}
    
    for word in words:
        w_lower = word.lower().strip("(),.-:;'\"")
        if not w_lower:
            continue
        if w_lower in SKIP_WORDS:
            if w_lower == "and":
                translated_parts.append("与")
            # 其他连接词直接省略
            continue
        if w_lower in DICT:
            val = DICT[w_lower]
            if "/" in val:
                val = val.split("/")[0]
            translated_parts.append(val)
        elif re.match(r'^[\u4e00-\u9fff]', word):
            # 已经是中文
            translated_parts.append(word)
        else:
            # 保留原文
            translated_parts.append(word)
    
    # 5. 组合结果
    result = "".join(p for p in translated_parts if p)
    
    # 6. 清理
    result = re.sub(r'\s+', '', result)
    result = re.sub(r'与{2,}', '与', result)
    result = result.strip("与")
    
    # 如果完全没有中文，视为翻译失败
    cn_chars = len(re.findall(r'[\u4e00-\u9fff]', result))
    if cn_chars == 0:
        return name
    
    return result


