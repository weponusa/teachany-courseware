def should_translate_name(name):
    """仅翻译以英文为主、且含待译英文词的节点名"""
    if not name or not re.search(r'[A-Za-z]{3,}', name):
        return False
    cn = len(re.findall(r'[\u4e00-\u9fff]', name))
    en = len(re.findall(r'[A-Za-z]', name))
    if cn > en:
        return False
    words = re.findall(r'[A-Za-z]+', name)
    for w in words:
        if len(w) >= 3 and w not in KEEP_EN and w.upper() not in KEEP_EN:
            return True
    return False


def is_acceptable_translation(original, result):
    """拒绝中英乱拼的劣质翻译"""
    if not result or result == original:
        return False
    cn_chars = len(re.findall(r'[\u4e00-\u9fff]', result))
    if cn_chars < 2:
        return False
    bad_en = [
        w for w in re.findall(r'[A-Za-z]{4,}', result)
        if w.upper() not in KEEP_EN
    ]
    if bad_en:
        return False
    return True


def main():
    import sys
    map_only = '--map-only' in sys.argv
    print("📋 加载数据...")
    map_data = json.loads(MAP_DATA.read_text("utf-8"))
    
    # 统计
    en_count = 0
    translated_count = 0
    failed = []
    
    print("🔄 翻译节点名称...")
    for node in map_data["nodes"]:
        name = node["name"]
        if not should_translate_name(name):
            continue
        
        en_count += 1
        cn_name = translate_name(name)
        
        if is_acceptable_translation(name, cn_name):
            if "name_en" not in node:
                node["name_en"] = name
            node["name"] = cn_name
            translated_count += 1
        else:
            failed.append(name)
    
    print(f"   英文节点: {en_count}")
    print(f"   成功翻译: {translated_count}")
    print(f"   保留原文: {len(failed)}")
    
    if failed:
        print(f"\n   前 20 个未翻译:")
        for f in failed[:20]:
            print(f"     {f}")
    
    from datetime import datetime, timezone
    map_data.setdefault("stats", {})["translated_at"] = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")

    # 保存
    print("\n📝 保存 knowledge-map-data.json...")
    MAP_DATA.write_text(json.dumps(map_data, ensure_ascii=False), "utf-8")

    if map_only:
        print("   (--map-only：跳过知识树 / nodes-metadata / _index 同步)")
        print("\n✅ 图谱数据已更新！")
        return
    
    # 同步更新知识树 JSON
    print("📝 更新知识树 JSON...")
    tree_updated = 0
    for f in TREES_DIR.glob("*.json"):
        if f.name == "_index.json":
            continue
        data = json.loads(f.read_text("utf-8"))
        changed = False
        for domain in data.get("domains", []):
            for node in domain.get("nodes", []):
                # 优先用 name_en 作为翻译源，否则用 name
                en_name = node.get("name_en", "") or node.get("name", "")
                if not should_translate_name(en_name):
                    continue
                cn = translate_name(en_name)
                if is_acceptable_translation(en_name, cn):
                    node["name_en"] = en_name
                    node["name"] = cn
                    changed = True
                    tree_updated += 1
        if changed:
            f.write_text(json.dumps(data, ensure_ascii=False, indent=2), "utf-8")
    
    print(f"   更新了 {tree_updated} 个节点")
    
    # 更新 nodes-metadata.json
    meta_file = Path(__file__).parent.parent / "data" / "nodes-metadata.json"
    if meta_file.exists():
        print("📝 更新 nodes-metadata.json...")
        meta = json.loads(meta_file.read_text("utf-8"))
        nodes_list = meta.get("nodes", []) if isinstance(meta, dict) else meta
        meta_updated = 0
        for entry in nodes_list:
            if not isinstance(entry, dict):
                continue
            name = entry.get("name", "")
            if re.search(r'[A-Za-z]{3,}', name):
                cn = translate_name(name)
                if cn != name:
                    entry["name_en"] = name
                    entry["name"] = cn
                    meta_updated += 1
        meta_file.write_text(json.dumps(meta, ensure_ascii=False), "utf-8")
        print(f"   更新了 {meta_updated} 条")
    
    # 更新 _index.json
    idx_file = TREES_DIR / "_index.json"
    if idx_file.exists():
        print("📝 更新 _index.json...")
        idx = json.loads(idx_file.read_text("utf-8"))
        idx_updated = 0
        for entry in idx:
            name = entry.get("name", "")
            if re.search(r'[A-Za-z]{3,}', name):
                cn = translate_name(name)
                if cn != name:
                    entry["name_en"] = name
                    entry["name"] = cn
                    idx_updated += 1
        idx_file.write_text(json.dumps(idx, ensure_ascii=False, indent=2), "utf-8")
        print(f"   更新了 {idx_updated} 条")
    
    print("\n✅ 全部完成！")


if __name__ == "__main__":
    main()
