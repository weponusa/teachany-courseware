#!/bin/bash
# 批量通过 NotebookLM 生成中文知识结构信息图 (V2 - 课标重点增强版)
# 用法: bash batch-infographic-v2.sh
# 每个课件的 prompt 包含完整课标知识重点

EXAMPLES="/Users/wepon/CodeBuddy/一次函数/teachany-opensource/examples"

SUCCESS=0
FAIL=0
TOTAL=13

# ============================================================
# 辅助函数
# ============================================================
process_course() {
  local dir="$1"
  local title="$2"
  local hero="$3"
  local prompt="$4"
  local idx="$5"

  echo ""
  echo "============================================================"
  echo "📚 [$idx/$TOTAL] $title ($dir)"
  echo "============================================================"

  # 1. 创建 notebook
  echo "  1️⃣  创建 Notebook..."
  NB_ID=$(notebooklm create "TeachAny-$title" 2>&1 | grep -oE '[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}' | head -1)
  if [ -z "$NB_ID" ]; then
    echo "  ❌ 创建 Notebook 失败"
    FAIL=$((FAIL+1))
    return
  fi
  echo "     ✅ ID: $NB_ID"

  # 2. 设置活跃 notebook
  notebooklm use "$NB_ID" > /dev/null 2>&1

  # 3. 添加课件源文件
  echo "  2️⃣  添加课件源文件..."
  notebooklm source add "$EXAMPLES/$dir/index.html" --type text --title "${title}互动课件" > /dev/null 2>&1
  if [ $? -ne 0 ]; then
    echo "  ❌ 添加 source 失败"
    FAIL=$((FAIL+1))
    return
  fi
  echo "     ✅ 已添加"

  # 等待 source 处理
  sleep 10

  # 4. 生成信息图
  echo "  3️⃣  生成信息图..."
  notebooklm generate infographic "$prompt" --language zh --detail detailed --orientation landscape --wait 2>&1 | tail -5
  if [ $? -ne 0 ]; then
    echo "  ❌ 生成信息图失败"
    FAIL=$((FAIL+1))
    return
  fi

  # 5. 下载信息图
  echo "  4️⃣  下载信息图..."
  TMPFILE="/tmp/nlm-info-$dir.webp"
  rm -f /tmp/nlm-info-$dir*.webp 2>/dev/null
  notebooklm download infographic "$TMPFILE" 2>&1 | tail -3

  # 处理可能的文件名后缀问题
  if [ ! -f "$TMPFILE" ]; then
    ACTUAL=$(ls /tmp/nlm-info-$dir*.webp 2>/dev/null | head -1)
    if [ -n "$ACTUAL" ]; then
      TMPFILE="$ACTUAL"
    else
      echo "  ❌ 下载失败"
      FAIL=$((FAIL+1))
      return
    fi
  fi

  # 6. 替换 hero 图
  cp "$TMPFILE" "$EXAMPLES/$dir/assets/$hero"
  echo "     ✅ 已保存: assets/$hero ($(du -h "$EXAMPLES/$dir/assets/$hero" | cut -f1))"
  rm -f /tmp/nlm-info-$dir*.webp

  SUCCESS=$((SUCCESS+1))
  echo "  ✅ 完成!"

  # 课件间间隔
  sleep 5
}

