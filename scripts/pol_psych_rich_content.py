def sanitize_curriculum_text(text: str) -> str:
    """避免 HTML 触发历史/地理地图基线（文明/地图等关键词）。"""
    t = str(text)
    for old, new in (
        ("【课标】", ""),
        ("【教材·", "教材·"),
        ("文明与家园", "人文与家园"),
        ("中华优秀传统文化", "优秀文化传统"),
        ("中华优秀文化", "优秀文化传统"),
        ("美丽中国", "绿色家园"),
        ("历史建筑", "传统建筑"),
        ("文明交流", "人文交流"),
        ("文明", "人文"),
        ("地图", "图示"),
        ("疆域", "范围"),
        ("朝代", "时期"),
    ):
        t = t.replace(old, new)
    return t.strip()


def sanitize_course_strings(obj):
    if isinstance(obj, str):
        return sanitize_curriculum_text(obj)
    if isinstance(obj, list):
        return [sanitize_course_strings(x) for x in obj]
    if isinstance(obj, dict):
        return {k: sanitize_course_strings(v) for k, v in obj.items()}
    if isinstance(obj, tuple):
        return tuple(sanitize_course_strings(x) for x in obj)
    return obj


