#!/usr/bin/env python3
"""生成元素周期表课件 TTS 语音"""
import asyncio, edge_tts, os

SEGS = [
    ("seg01", "模块1：怎么排好队。你已经认识了很多元素，但100多种元素散落各处找不到规律。门捷列夫发明了元素周期表，让所有元素各就各位。排列规则：按原子序数递增排列。原子序数等于质子数等于核外电子数，它们是三胞胎。"),
    ("seg02", "模块2：为什么排在一起？看看第1A族，锂、钠、钾都是活泼金属。再看第7A族，氟、氯都是活泼非金属。原因是：同族元素最外层电子数相同，而最外层电子数决定化学性质。但注意：同族不等于相同，从上到下金属性增强。"),
    ("seg03", "模块3：同周期元素性质怎么变？同周期就是同一横行，电子层数相同。从左到右最外层电子数递增，失电子能力减弱，得电子能力增强。口诀：同周期从左到右，金属性减弱，非金属性增强。"),
    ("seg04", "模块4：如何利用化学地图预测性质？门捷列夫曾在周期表中预留空位，成功预言了未发现的元素。你也能做预测：知道位置就能推测性质。周期序号等于电子层数，族序号等于最外层电子数。掌握这两个对应关系，就能预测未知元素的性质趋势。"),
]

async def gen():
    os.makedirs("tts", exist_ok=True)
    for sid, text in SEGS:
        path = f"tts/{sid}_zh.mp3"
        comm = edge_tts.Communicate(text, "zh-CN-XiaoxiaoNeural")
        await comm.save(path)
        print(f"✅ {path}")

asyncio.run(gen())
