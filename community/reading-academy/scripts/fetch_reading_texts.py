def extract_from_large_file(source_file, target_title, section_marker):
    """从大文件中提取特定故事"""
    src_path = f"texts/快乐读书吧/{source_file}"
    if not os.path.exists(src_path):
        return None
    
    with open(src_path, 'r', encoding='utf-8') as f:
        content = f.read()
    
    # 找故事起始位置
    idx = content.find(section_marker)
    if idx < 0:
        return None
    
    # 找下一个故事起始
    rest = content[idx + len(section_marker):]
    # 找常见的下一个故事标记
    markers = [f"\n\n\n\n", "──────────────", "══════════════"]
    end_idx = len(content)
    for marker in markers:
        pos = rest.find(marker)
        if 0 < pos < 5000:
            end_idx = idx + len(section_marker) + pos + len(marker)
            break
    
    text = content[idx:end_idx].strip()
    return text


